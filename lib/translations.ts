export const getTranslation = (key: string, language: string): string => {
    if (language === "vi") return key; // Return original Vietnamese text
    return translations[key]?.[language] || key;
};

export const translateText = (text: string, language: string): string => {
    if (language === "vi") return text;

    // Try exact match first
    if (translations[text]?.[language]) {
        return translations[text][language];
    }

    // Try to find partial matches for longer texts
    for (const key in translations) {
        if (text.includes(key) && translations[key][language]) {
            return text.replace(key, translations[key][language]);
        }
    }

    return text; // Return original if no translation found
};
